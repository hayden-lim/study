package variable; // 소속된 패키지 선언

public class Var1 {
    public static void main(String[] args) {
        // 변수 (variable)

        System.out.println(10); //변경 10->20->10
        System.out.print(10); //변경 10->20->10, println -> print로 변경(줄바꿈 없음)
        System.out.println(10); //변경 10->20

    }
}
